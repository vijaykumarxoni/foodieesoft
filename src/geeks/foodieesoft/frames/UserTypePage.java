    public void showInTable() {
        tableModelUserType.setRowCount(0);

        user_type_list = dbManager.getAllUserTypes();
        for (UserType userType : user_type_list) {
            Vector V = new Vector();
            V.add(userType.getUser_type());
            tableModelUserType.addRow(V);
        }
    }

    public void checkPermissions(int user_cat_id) {
        ArrayList<Permissions> permissions_name_list;
        permissions_name_list = dbManager.getUserPermissionName(user_cat_id);

        for (Permissions permissions : permissions_name_list) {
            if ((permissions.getPermission_name().equalsIgnoreCase("Add User"))) {
                this.jButtonSave.setEnabled(true);
            } else if ((permissions.getPermission_name().equalsIgnoreCase("Update User"))) {
                this.jButtonUpdate.setEnabled(true);

            } else if ((permissions.getPermission_name().equalsIgnoreCase("Delete User"))) {
                this.jButtonDelete.setEnabled(true);

            }

        }
    }
