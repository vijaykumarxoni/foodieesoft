    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        if (!(this.jTextFieldUserType.getText().equals("Enter UserType") || this.jTextFieldUserType.getText().equals(""))) {

            int status = dbManager.saveUserType(new UserType(user_id, CurrentDate.getCurrentDate(), this.jTextFieldUserType.getText()));
            showInTable();
            this.jTextFieldUserType.setText("Enter UserType");
            if (status != 0) {
                JOptionPane.showMessageDialog(this, "Successfully added");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please fill required field");
            this.jTextFieldUserType.setText("Enter UserType");

        }
    }//GEN-LAST:event_jButtonSaveActionPerformed

    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed

        if (!(this.jTextFieldUserType.getText().equals("Enter UserType") || this.jTextFieldUserType.getText().equals(""))) {

            int user_cat_id = user_type_list.get(this.jTableUserType.getSelectedRow()).getUser_cat_id();
            int i = dbManager.modifyUserType(new UserType(user_cat_id, 0, 0, user_id, "", CurrentDate.getCurrentDate(),
                    this.jTextFieldUserType.getText()));

            if (i != 0) {
                JOptionPane.showMessageDialog(this, "Updated Sucessfully");
                this.jTextFieldUserType.setText("Enter UserType");
                this.jButtonUpdate.setVisible(false);
                this.jButtonDelete.setVisible(false);
                this.jButtonSave.setVisible(true);

                showInTable();

            }
        } else {
            JOptionPane.showMessageDialog(this, "Please fill required field");
            this.jTextFieldUserType.setText("Enter UserType");

        }


    }//GEN-LAST:event_jButtonUpdateActionPerformed

